/**
 * Get a translation function for the given locale
 * Supports interpolation: t("key", { count: 5 }) replaces {count} with 5
 */
export function getTranslation(locale: Locale = "es") {
  const dict = translations[locale] || translations.es;

  return function t(key: string, params?: Record<string, string | number>): string {
    let value = (dict as any)[key] || (translations.es as any)[key] || key;

    if (params) {
      for (const [k, v] of Object.entries(params)) {
        value = value.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
      }
    }

    return value;
  };
}

/**
 * Hook-style usage (for React components)
 */
export function useTranslation(locale: Locale = "es") {
  return getTranslation(locale);
}
